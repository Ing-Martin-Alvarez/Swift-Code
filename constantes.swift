En Swift, las constantes se declaran utilizando la palabra clave let. 
Las constantes son valores inmutables, lo que significa que una vez que 
les asignas un valor, no puedes cambiarlo posteriormente. 
Esto es útil cuando tienes valores que no deberían modificarse después 
de su inicialización.
*/

// Declaración de una constante llamada "pi" con valor 3.14159
let pi = 3.14159  
print("La constante pi es :\(pi)") 
// Declaración de una constante llamada "nombre" con valor "Juan"
let nombre = "Juan" 
print("El nombre es : \(nombre) ")
// Declaración de una constante llamada "edad" con valor 30 
let edad = 30 
let casado = false
let numero: Int = 234

print("El número es : \(numero) ")
